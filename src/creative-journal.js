// Fallback for unknown subtypes
const FALLBACK = JOURNAL.manga;

export function getCreativeJournal(project, hasPartner) {
  const subtype = project.subtype || 'manga';
  const journal = JOURNAL[subtype] || FALLBACK;
  const ratio = project.progress / project.needed;
  const pool = ratio < 0.5 ? journal.early : journal.late;
  const entries = [pool[Math.floor(Math.random() * pool.length)]];
  if (hasPartner && Math.random() < 0.4) {
    entries.push(journal.partner[Math.floor(Math.random() * journal.partner.length)]);
  }
  return entries;
}

export function getJournalMilestone(project) {
  const subtype = project.subtype || 'manga';
  const journal = JOURNAL[subtype] || FALLBACK;
  return journal.milestone;
}
